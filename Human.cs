using System;

class Human
{
  public string sex;
  public string race;
  public string place;
  public string name;
  public int age;
  public int height;

  public Human(string humanSex, string humanRace, string humanPlace, string humanName, int humanAge, int humanHeight)
  {
    this.sex = humanSex;
    this.race = humanRace;
    this.place = humanPlace;
    this.name = humanName;
    this.age = humanAge;
    this.height = humanHeight;
  }
}
